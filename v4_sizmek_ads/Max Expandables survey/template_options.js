/****************************/
/***** Carousel options *****/
/****************************/

/* 
amzLoop (type : Boolean)
- Determines if the Carousel is looping when the user clicks on previous and next arrows on the first and last slides.
- Value : true (Carousel will loop) or false (Carousel will not loop)   
- Default : true
*/
var amzLoop = true;


/*
amzBulletsVisibility (type : Boolean)
- Determines if the navigation bullets are visible or not
- Value : true (bullets are visible) or false (bullets are hidden)
- Default : true
*/
var amzBulletsVisibility = true;


/* 
amzAutoPlay(type : Boolean)
- Determines if the Carousel is automatically animated once initiated.
- Value : true (turning on autoplaying) or false (turning off the autoplaying)
- Default : true
*/
var amzAutoPlay = true;


/*
amzAutoPlayDelay (type : Integer)
- Time spent in seconds on each slide when the Carousel is autoplaying (will be ignored if autoplay is turned off)
- Value : numbers (e.i. 1, 2, 3...)
- Default : 3
*/
var amzAutoPlayDelay = 3;


/*
amzDirection (type : String)
- Determines the direction of the Carousel
- Value : 'horizontal' (left is previous, right is next) or 'vertical' (top is previous, bottom is next)
- Default : 'horizontal' 
*/
var amzDirection = 'horizontal';


/****************************/
/****** Design options ******/
/****************************/

/*
amzBorder (type : String)
- Determines the color of the 1px border around the creative, or if it should be disabled
- Value
	- for a color: RGB, HEX or predefined color names such as 'black' or 'white'
	- to disable the border: 'transparent' or 'none'
- Default : 'grey'
*/
var amzBorder = 'grey';


/*
amzButtonsColor (type : String)
- Determines the main color of the buttons and bullets in the Carousel
- Value : RGB, HEX or predefined color names such as 'black' or 'white'
- Default : 'grey'
*/
var amzButtonsColor = 'grey';




